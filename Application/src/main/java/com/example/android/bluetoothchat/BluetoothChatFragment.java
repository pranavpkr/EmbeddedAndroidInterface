    public void msg(CharSequence a){Toast.makeText(getContext(), a, Toast.LENGTH_SHORT).show();}


    class CountDownRunner implements Runnable{
        // @Override
        public void run() {
            while(!Thread.currentThread().isInterrupted()){
                try {
                    mydate = new SimpleDateFormat("yyyy-MM-dd , hh:mm:ss a ,").format(new Date());
                    Thread.sleep(1000); // Pause of 1 Second
                } catch (InterruptedException e) {
                    Thread.currentThread().interrupt();
                }catch(Exception e){
                }
            }
        }
    }
